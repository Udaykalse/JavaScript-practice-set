// 1. Array Manipulation Basics
let fruits =['apple','banana'];
console.log(`Initial Array:- ${fruits}`);

fruits.push('orange');
console.log(`After push('orange'): ${fruits}`);

fruits.unshift('grape');
console.log(fruits);

fruits.pop();
console.log(fruits);

fruits.shift();
console.log(fruits);